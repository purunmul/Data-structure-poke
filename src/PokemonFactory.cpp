const PokemonData POKEMON_LIST[] = {
    {"파이리", 50, 14, 6, 12, ElementType::Fire, CHARMANDER_SPRITE}
};

const int POKEMON_COUNT = sizeof(POKEMON_LIST) / sizeof(POKEMON_LIST[0]);
